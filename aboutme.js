//Details anzeigen//
function showDetails() {
    var detailsContainer = document.getElementById("details");
    detailsContainer.style.display = "block";
  }
  
  //Details verstecken//
  function hideDetails() {
    var detailsContainer = document.getElementById("details");
    detailsContainer.style.display = "none";
  }
  